# this code about is Slicing string
#SLICING
b = "Hello,World!"
print(b[2:5])
b = "Hello,World!"
print(b[5])
#Slice From the Start
b = "Hello,World!"
print(b[:5])
b = "Hello,World!"
print(b[5:])
#Slice To the End
b = "Hello, World!"
print(b[2:])
b = "Hello,World!"
print(b[:2])
b = "Hello,World!"
print(b[2])
b = "Hello, World!"
#Negative Indexing
print(b[-5:-2])
b = "Hello, World!"
print(b[-5:+2])
b = "Hello, World!"
print(b[5:2])

